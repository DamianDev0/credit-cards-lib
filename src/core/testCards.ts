export function getTestCardsByBrand(brand: CardBrand): TestCard[] {
  return TEST_CARDS.filter((card) => card.brand === brand);
}

export function getTestCardsByType(type: CardType): TestCard[] {
  return TEST_CARDS.filter((card) => card.type === type);
}

export function getTestCardsByLevel(level: CardLevel): TestCard[] {
  return TEST_CARDS.filter((card) => card.level === level);
}

export function getRandomTestCard(): TestCard {
  return TEST_CARDS[Math.floor(Math.random() * TEST_CARDS.length)];
}

export function findTestCard(query: string): TestCard | undefined {
  const lowerQuery = query.toLowerCase();
  return TEST_CARDS.find((card) => card.description.toLowerCase().includes(lowerQuery));
}
